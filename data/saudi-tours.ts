// Helper functions
export function getAllTours(): Tour[] {
  return saudiTours
}

export function getTourBySlug(slug: string): Tour | undefined {
  return saudiTours.find((tour) => tour.slug === slug)
}

export function getToursByCategory(category: string): Tour[] {
  return saudiTours.filter((tour) => tour.category === category)
}

export function getToursByDuration(duration: number): Tour[] {
  return saudiTours.filter((tour) => tour.duration === duration)
}

export function getToursByDifficulty(difficulty: string): Tour[] {
  return saudiTours.filter((tour) => tour.difficulty === difficulty)
}

export function searchTours(query: string): Tour[] {
  const lowercaseQuery = query.toLowerCase()
  return saudiTours.filter(
    (tour) =>
      tour.title.toLowerCase().includes(lowercaseQuery) ||
      tour.shortDescription.toLowerCase().includes(lowercaseQuery) ||
      tour.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)),
  )
}

export function getFeaturedTours(): Tour[] {
  return saudiTours.filter((tour) => tour.originalPrice && tour.originalPrice > tour.price)
}

export function getToursByPriceRange(minPrice: number, maxPrice: number): Tour[] {
  return saudiTours.filter((tour) => tour.price >= minPrice && tour.price <= maxPrice)
}
